#multiplies two numbers
def multiply(a, b):
    return a * b

#adds two numbers
def add(a, b):
    return a + b

#subtracts second input from first input
def subtract(a, b):
    return a - b

#divides first input by second input
def divide(a, b):
    return a / b

#returns the square of an input
def square(a):
    return a ** 2